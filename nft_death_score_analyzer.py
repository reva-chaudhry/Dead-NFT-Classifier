# ── CLI entry point ───────────────────────────────────────────────────────────

def main():
    """
    Reads a CSV of NFT collections, scores each one, and writes results
    to both a flat CSV and a full JSON file.

    Input CSV expected columns:
      contract_address, name, twitter_handle, discord_invite,
      opensea_slug, opensea_link, last_sale_time, token_count

    Output:
      nft_death_scores.csv  — flat CSV, one row per collection
      nft_death_scores.json — full nested JSON with all metric details
    """
    parser = argparse.ArgumentParser(description="NFT Death Score Analyzer v12")
    parser.add_argument("--input",  default="input_socials.csv") # INPUT CSV = entire collection of nfts to score
    parser.add_argument("--output", default="nft_death_scores.csv")
    parser.add_argument("--json",   default="nft_death_scores.json")
    args, _ = parser.parse_known_args()

    # Warn about missing optional API keys
    missing = [k for k, v in {
        "ALCHEMY_API_KEY":    ALCHEMY_API_KEY,
        "OKX_API_KEY":        OKX_API_KEY,
        "OKX_API_SECRET":     OKX_API_SECRET,
        "OKX_API_PASSPHRASE": OKX_API_PASSPHRASE,
    }.items() if not v]
    if missing:
        log.warning("Missing required keys (metrics will be skipped): %s", missing)
    if not APIFY_API_TOKEN:
        log.warning("APIFY_API_TOKEN not set – Twitter metrics skipped")
    if not OPENSEA_API_KEY:
        log.warning("OPENSEA_API_KEY not set – OpenSea fallback disabled")

    scorer = NFTDeathScorer()
    rows, full_results = [], []

    with open(args.input, newline="", encoding="utf-8-sig") as f:
        reader = csv.DictReader(f)
        reader.fieldnames = [name.strip().lower() for name in reader.fieldnames]
        collections = list(reader)

    log.info("Loaded %d collections from %s", len(collections), args.input)

    for col in tqdm(collections, desc="Scoring"):
        contract = str(col.get("contract_address") or "").strip()
        name     = str(col.get("name") or "").strip()
        if not contract:
            log.warning("Skipping row with no contract: %s", name)
            continue
        try:
            result = scorer.score_collection(
                contract            = contract,
                name                = name,
                twitter_handle      = str(col.get("twitter_handle") or "").strip(),
                discord_invite      = str(col.get("discord_invite") or "").strip(),
                opensea_slug        = str(col.get("opensea_slug") or "").strip(),
                opensea_link        = str(col.get("opensea_link") or "").strip(),
                dune_token_count    = int(col.get("token_count", 0) or 0),
                dune_last_sale_time = str(col.get("last_sale_time") or "").strip(),
            )
            full_results.append(result)
            rows.append(result_to_row(result, col))
        except Exception as exc:
            log.error("Failed to score %s (%s): %s", name, contract, exc)
            rows.append(error_row(contract, col, str(exc)))
        time.sleep(0.5)

    # Write CSV output
    if rows:
        df = pd.DataFrame(rows)
        metric_cols = []
        for p in METRIC_PREFIXES:
            metric_cols += [f"{p}_label", f"{p}_category", f"{p}_max_pts",
                            f"{p}_triggered", f"{p}_skipped",
                            f"{p}_points",    f"{p}_detail"]
        ordered = [c for c in FIXED_COLS + CONFIDENCE_COLS + metric_cols
                   if c in df.columns]
        df[ordered].to_csv(args.output, index=False)
        log.info("CSV → %s  (%d rows, %d columns)", args.output, len(df), len(ordered))

    # Write JSON output
    with open(args.json, "w") as f:
        json.dump(full_results, f, indent=2, default=str)
    log.info("JSON → %s", args.json)

    # Console summary table
    print("\n" + "=" * 90)
    print(f"{'Name':<28} {'Raw':>4} {'Max':>4} {'Score':>6}  "
          f"{'Verdict':<22}  {'Conf':<7}  Wash%")
    print("=" * 90)
    for row in rows:
        name    = str(row.get("name", row.get("contract", "")))[:27]
        print(f"{name:<28} {str(row.get('raw_score','')):>4} "
              f"{str(row.get('max_available','')):>4} "
              f"{str(row.get('rescaled_score','ERR')):>6}  "
              f"{str(row.get('verdict',''))[:21]:<22}  "
              f"{str(row.get('confidence_overall',''))::<7}  "
              f"{str(row.get('wash_ratio',''))}")
    print("=" * 90)

    verdicts = [r.get("verdict", "") for r in rows
                if "ERROR" not in str(r.get("verdict", ""))]
    di_count = sum(1 for v in verdicts if v.startswith("Data Insufficient"))
    print(f"\n  DEAD:                 {verdicts.count('DEAD'):>4}")
    print(f"  Institutional Decay:  {verdicts.count('Institutional Decay'):>4}")
    print(f"  Stressed/Declining:   {verdicts.count('Stressed / Declining'):>4}")
    print(f"  Alive or Recoverable: {verdicts.count('Alive or Recoverable'):>4}")
    print(f"  Data Insufficient:    {di_count:>4}")
    print(f"  Errors:               "
          f"{sum(1 for r in rows if 'ERROR' in str(r.get('verdict', ''))):>4}")
    print(f"\n  Output: {args.output}  |  {args.json}\n")


