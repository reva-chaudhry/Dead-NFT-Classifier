    def trade_history(self, contract: str, days: int = 90) -> list:
        """
        Fetches trade history for the past `days` days, stopping as soon
        as trades older than the cutoff are encountered.
        """
        path      = "/api/v5/mktplace/nft/markets/trades"
        items     = []
        cursor    = None
        cutoff_ms = _days_ago_ts(days) * 1000
        while True:
            params = {"chain": OKX_ETH_CHAIN,
                      "collectionAddress": contract.lower().strip(), "limit": "50"}
            if cursor:
                params["cursor"] = cursor
            data  = _okx_get(path, params)
            inner = data.get("data", {})
            batch = inner.get("data", []) if isinstance(inner, dict) else (inner or [])
            if not batch:
                break
            filtered, stop = [], False
            for t in batch:
                ts = int(t.get("dealTime") or t.get("tradeTime") or 0)
                if ts >= cutoff_ms:
                    filtered.append(t)
                else:
                    stop = True
                    break
            items.extend(filtered)
            cursor = inner.get("cursor") if isinstance(inner, dict) else None
            if stop or not cursor:
                break
        return items

    def daily_volume_proxy(self, trades_90: list) -> list:
        """
        Aggregates trade history into a per-day volume and sales-count
        structure covering the last 91 days. Day 0 = today, day 90 = 90
        days ago. Used by the trade frequency and volume metrics.
        """
        today  = datetime.now(timezone.utc).date()
        by_day = defaultdict(lambda: {"volume": 0.0, "sales_count": 0})
        for t in trades_90:
            ts = int(t.get("dealTime") or t.get("tradeTime") or 0)
            if ts == 0:
                continue
            if ts > 1e10:
                ts = ts / 1000
            day_index = (today - datetime.fromtimestamp(ts, tz=timezone.utc).date()).days
            price     = float(t.get("dealPrice") or t.get("price") or 0)
            by_day[day_index]["volume"]      += price
            by_day[day_index]["sales_count"] += 1
        return [{"day_index": i, **by_day[i]} for i in range(91)]


