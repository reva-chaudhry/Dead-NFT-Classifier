# ── Generic HTTP helpers ──────────────────────────────────────────────────────

def _get(url, params=None, headers=None, retries=3, timeout=20):
    """
    GET request with retry logic and rate-limit handling.
    Returns parsed JSON dict, or {} on failure.
    404s are returned immediately without retrying — they are definitive.
    429s trigger exponential backoff before retrying.
    """
    for attempt in range(retries):
        try:
            r = requests.get(url, params=params, headers=headers, timeout=timeout)
            if r.status_code == 429:
                wait = 2 ** attempt
                log.warning("Rate-limited – sleeping %ss", wait)
                time.sleep(wait)
                continue
            if r.status_code == 404:
                return {}   # definitive not-found, no value in retrying
            r.raise_for_status()
            return r.json()
        except Exception as exc:
            log.warning("Attempt %s/%s failed (%s): %s", attempt + 1, retries, url, exc)
            time.sleep(1)
    return {}


def _days_ago_ts(n: int) -> int:
    """Returns a Unix timestamp for n days ago (UTC), used as API cutoff filters."""
    return int((datetime.now(timezone.utc) - timedelta(days=n)).timestamp())


def _sale_age_days(sale: dict) -> float:
    """
    Returns how many days ago a sale occurred.
    Handles both millisecond and second timestamps.
    Returns 999.0 when no timestamp is present (treated as very old).
    """
    ts = sale.get("timestamp") or sale.get("tradeTime") or 0
    if ts:
        return (datetime.now(timezone.utc) -
                datetime.fromtimestamp(int(ts) / 1000 if int(ts) > 1e10 else int(ts),
                                       tz=timezone.utc)).total_seconds() / 86400
    return 999.0


def _verdict(score: int, metrics_with_data: int = 10) -> str:
    """
    Converts a rescaled score into a human-readable verdict.

    Logic:
      1. If score >= DI_DEAD_OVERRIDE (16), call it DEAD regardless of data
         coverage — the signal is too strong to be explained by missing data.
      2. If fewer than DATA_INSUFFICIENT_METRIC_THRESHOLD metrics have data,
         flag as Data Insufficient with the partial score shown for reference.
      3. Otherwise apply standard band thresholds.
    """
    if score >= DI_DEAD_OVERRIDE:
        return "DEAD"
    if metrics_with_data < DATA_INSUFFICIENT_METRIC_THRESHOLD:
        return f"Data Insufficient (partial score: {score})"
    if score >= 11: return "DEAD"
    if score >= 7:  return "Decaying"
    if score >= 4:  return "Stressed / Declining"
    return "Alive or Recoverable"


def _is_skipped(metric: dict) -> bool:
    """
    Returns True when a metric was skipped due to missing data.
    Skipped metrics are excluded from max_available so they don't
    artificially inflate or deflate the rescaled score.
    """
    detail = metric.get("detail", "").lower()
    return any(w in detail for w in (
        "skipped", "missing", "not_provided", "no_api_key",
        "no_handle", "insufficient", "no_supply", "no_floor",
        "no_asks", "no_ownership", "no_data", "no_sales",
        "no_bids", "no_okx", "no_twitter", "no_discord",
        "fetch_failed",
    ))


def _rate(value: float, high_thresh: float, low_thresh: float) -> str:
    """Maps a numeric value to HIGH / MEDIUM / LOW confidence tier."""
    if value >= high_thresh: return "HIGH"
    if value >= low_thresh:  return "MEDIUM"
    return "LOW"


def _rescale(raw_score: int, max_available: int) -> int:
    """
    Normalises raw_score against the points that were actually scoreable
    (max_available), then scales to RESCALE_NORM (22).

    This prevents dead collections from appearing artificially healthy just
    because many metrics had to be skipped due to missing market data.
    A collection that scores 8/10 available points rescales to ~18/22,
    not 8/20.
    """
    if max_available <= 0:
        return 0
    return round((raw_score / max_available) * RESCALE_NORM)


# ── OKX Authentication ────────────────────────────────────────────────────────

def _okx_headers(method: str, path: str, body: str = "") -> dict:
    """
    Builds OKX API authentication headers using HMAC-SHA256 signing.
    OKX requires a timestamp, signature, and passphrase on every request.
    """
    ts = datetime.now(timezone.utc).isoformat(timespec='milliseconds').replace('+00:00', 'Z')
    prehash = ts + method.upper() + path + body
    sig = base64.b64encode(
        hmac.new(OKX_API_SECRET.encode('utf-8'),
                 prehash.encode('utf-8'),
                 hashlib.sha256).digest()
    ).decode()
    return {
        "OK-ACCESS-KEY":        OKX_API_KEY,
        "OK-ACCESS-SIGN":       sig,
        "OK-ACCESS-TIMESTAMP":  ts,
        "OK-ACCESS-PASSPHRASE": OKX_API_PASSPHRASE,
        "OK-ACCESS-PROJECT":    OKX_PROJECT_ID,
        "Content-Type":         "application/json",
    }


def _okx_get(path: str, params: dict = None) -> dict:
    """
    Authenticated GET request to the OKX NFT API.
    Builds the signed URL, calls _get(), and validates the OKX response code.
    Returns {} when credentials are missing or the API returns an error.
    """
    if not all([OKX_API_KEY, OKX_API_SECRET, OKX_API_PASSPHRASE, OKX_PROJECT_ID]):
        log.warning("OKX credentials incomplete — skipping request to %s", path)
        return {}
    query = ""
    if params:
        query = "?" + "&".join(f"{k}={v}" for k, v in sorted(params.items()))
    headers = _okx_headers("GET", path + query)
    data    = _get(f"https://web3.okx.com{path}{query}", params=None, headers=headers)
    if isinstance(data, dict):
        if data.get("code") == 0:
            return data
        if data:
            log.warning("OKX non-zero response: code=%s msg=%s",
                        data.get("code"), data.get("msg", ""))
    return {}


