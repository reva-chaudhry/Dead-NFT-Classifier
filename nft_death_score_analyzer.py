    @staticmethod
    def floor_price_eth(details: dict, asks: list = None, trades: list = None) -> float:
        """
        Returns the floor price in ETH using a three-tier fallback chain:
          Tier 1 — OKX detail floorPrice field (most reliable)
          Tier 2 — Lowest active ask price (when floor field is missing)
          Tier 3 — Most recent trade price (last resort for dead collections)

        Without this fallback, liq_volume, fin_bid_depth, and inv_near_floor
        all skip on collections where OKX returns floor=0.
        """
        try:
            fp = float(details.get("floorPrice") or 0)
            if fp > 0:
                return fp
        except Exception:
            pass

        if asks:
            prices = [float(a.get("price", 0) or a.get("unitPrice", 0) or 0)
                      for a in asks]
            prices = [p for p in prices if p > 0]
            if prices:
                floor = min(prices)
                log.info("Floor proxied from lowest ask: %.6f ETH", floor)
                return floor

        if trades:
            for t in sorted(trades,
                            key=lambda x: int(x.get("dealTime") or x.get("tradeTime") or 0),
                            reverse=True):
                try:
                    p = float(t.get("dealPrice") or t.get("price") or 0)
                    if p > 0:
                        log.info("Floor proxied from recent trade: %.6f ETH", p)
                        return p
                except Exception:
                    pass

        return 0.0

