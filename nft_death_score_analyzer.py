def result_to_row(result: dict, dune_row: dict) -> dict:
    """
    Flattens a score_collection() result dict into a flat CSV row.
    Merges in original input fields from the Dune CSV row so the output
    is self-contained for downstream analysis.
    """
    row = {
        "contract":               result["contract"],
        "name":                   result.get("name", ""),
        "image_url":              result.get("image_url", ""),
        "opensea_link":           dune_row.get("opensea_link", ""),
        "standard":               dune_row.get("standard", ""),
        "first_mint_time":        dune_row.get("first_mint_time", ""),
        "dune_token_count":       dune_row.get("token_count", ""),
        "dune_total_sales":       dune_row.get("total_sales", ""),
        "dune_last_sale_time":    dune_row.get("last_sale_time", ""),
        "dune_volume_usd_all_time": dune_row.get("volume_usd_all_time", ""),
        "twitter_handle":         dune_row.get("twitter_handle", ""),
        "discord_invite":         dune_row.get("discord_invite", ""),
        "floor":                  result.get("floor", ""),
        "floor_currency":         result.get("floor_currency", ""),
        "market_source":          result.get("market_source", "OKX"),
        "supply":                 result.get("supply", ""),
        "wash_ratio":             result.get("wash_ratio", ""),
        "timestamp":              result.get("timestamp", ""),
        "raw_score":              result.get("raw_score", ""),
        "skipped_pts":            result.get("skipped_pts", ""),
        "max_available":          result.get("max_available", ""),
        "rescaled_score":         result.get("rescaled_score", ""),
        "verdict":                result.get("verdict", ""),
    }
    for col in CONFIDENCE_COLS:
        row[col] = result.get("confidence", {}).get(col, "")
    metric_map = {m["key"]: m for m in result.get("metrics", [])}
    for prefix in METRIC_PREFIXES:
        m = metric_map.get(prefix, {})
        row[f"{prefix}_label"]     = METRIC_LABELS.get(prefix, "")
        row[f"{prefix}_max_pts"]   = METRIC_MAX_PTS.get(prefix, "")
        row[f"{prefix}_category"]  = METRIC_CATEGORY.get(prefix, "")
        row[f"{prefix}_triggered"] = "YES" if m.get("triggered") else ("NO" if m else "")
        row[f"{prefix}_skipped"]   = "YES" if _is_skipped(m) else "NO"
        row[f"{prefix}_points"]    = m.get("points", "")
        row[f"{prefix}_detail"]    = m.get("detail", "")
    return row


