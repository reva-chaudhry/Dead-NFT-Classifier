class OKXClient:
    """
    Fetches NFT collection data from the OKX marketplace API.

    OKX uses its own internal slug system that bears no relation to OpenSea
    slugs. collection_details() uses five successive strategies to resolve
    a slug before giving up, falling back to partial asset-level data.
    """

    def collection_details(self, contract: str, slug: str = "",
                           opensea_link: str = "",
                           collection_name: str = "") -> dict:
        """
        Resolves a collection slug and fetches full detail from OKX.

        Resolution strategies (tried in order, first success wins):
          1. OKX asset endpoint      — pulls slug from a single token response
          2. Parse opensea_link      — extracts slug from /collection/<slug> URLs
          3. OKX contract search     — searches OKX by contract address
          4. OKX keyword search      — searches OKX by collection display name
          5. Caller-supplied slug    — normalised to lowercase-hyphen

        Falls back to partial asset-level data (supply available but no
        floor/stats) if all strategies fail to reach the detail endpoint.
        """
        contract      = contract.lower().strip()
        clean_slug    = str(slug).strip()
        okx_slug      = ""
        fallback_data = {}

        # Strategy 1: pull slug from a token-level asset response
        asset_data = _okx_get("/api/v5/mktplace/nft/asset/list", {
            "chain": OKX_ETH_CHAIN, "contractAddress": contract, "limit": "1",
        })
        items = asset_data.get("data", [])
        if isinstance(items, dict):
            items = items.get("data", [])
        if items and isinstance(items, list) and items[0]:
            col_obj       = items[0].get("collection", {})
            okx_slug      = col_obj.get("slug", "")
            fallback_data = col_obj
            if okx_slug:
                log.info("OKX slug from asset endpoint: %s", okx_slug)

        # Strategy 2: extract from opensea_link if it contains /collection/<slug>
        if not okx_slug:
            link = str(opensea_link).strip()
            if "/collection/" in link:
                os_slug = link.rstrip("/").split("/collection/")[-1].split("/")[0]
                if os_slug and os_slug.lower() not in ("", "nan", "none"):
                    okx_slug = os_slug
                    log.info("OKX slug from opensea_link: %s", okx_slug)

        # Strategy 3: OKX contract address search
        if not okx_slug:
            search_data = _okx_get("/api/v5/mktplace/nft/collection/search", {
                "chain": OKX_ETH_CHAIN, "contractAddress": contract, "limit": "1",
            })
            s_items = search_data.get("data", [])
            if isinstance(s_items, dict):
                s_items = s_items.get("data", [])
            if isinstance(s_items, list) and s_items:
                okx_slug = s_items[0].get("slug", "")
                if okx_slug:
                    log.info("OKX slug from contract search: %s", okx_slug)

        # Strategy 4: OKX keyword search by collection display name.
        # Strips common suffixes (NFT, Official, Collection) that differ
        # between platforms, then matches by contractAddress in results.
        if not okx_slug and collection_name and \
                str(collection_name).lower() not in ("", "nan", "none"):
            keyword = re.sub(r'\s+(nft|official|collection)s?$', '',
                             collection_name.strip(), flags=re.IGNORECASE)
            kw_data = _okx_get("/api/v5/mktplace/nft/collection/search", {
                "chain": OKX_ETH_CHAIN, "keyword": keyword[:50], "limit": "5",
            })
            k_items = kw_data.get("data", [])
            if isinstance(k_items, dict):
                k_items = k_items.get("data", [])
            if isinstance(k_items, list) and k_items:
                matched = next(
                    (x for x in k_items
                     if (x.get("contractAddress") or "").lower() == contract),
                    k_items[0]
                )
                okx_slug = matched.get("slug", "")
                if okx_slug:
                    log.info("OKX slug from keyword search (%r): %s", keyword, okx_slug)

        # Strategy 5: normalise and use the caller-supplied slug directly
        if not okx_slug and clean_slug and clean_slug.lower() not in ("nan", "none", ""):
            okx_slug = clean_slug.lower().replace(" ", "-")
            log.info("OKX slug from caller-supplied value: %s", okx_slug)

        # Fetch full collection detail using the resolved slug
        if okx_slug:
            detail_data = _okx_get("/api/v5/mktplace/nft/collection/detail",
                                   {"slug": okx_slug})
            d_items = detail_data.get("data", [])
            if isinstance(d_items, dict):
                d_items = [d_items]
            if isinstance(d_items, list) and d_items:
                res   = d_items[0]
                stats = res.get("stats", {})
                if isinstance(stats, dict):
                    res.update(stats)
                if res:
                    return res

        if fallback_data:
            return fallback_data

        log.warning("OKX: no collection data for %s (tried slug=%s)", contract[:12], okx_slug)
        return {}

